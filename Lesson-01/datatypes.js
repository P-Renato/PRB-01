/*
Js primitive types ( String, Number, Boolean, Undefined, Null)
- String (anything wrapped in quotes) "Hey", "123", "true"
- Number (any numeric value) 123, 123.45
- Boolean: true or false

We will see object data types at a later time
In order to check the type of a data we use typeof
*/

const greeting = "Hello";
console.log( typeof greeting); // string

const myNumber = 3;
console.log( typeof myNumber); // number

console.log(typeof "3"); // string.. cos the numeric value is wrapped in quotes

const hungry = true;
console.log( typeof hungry); // boolean